    @Override
    public final ChannelPipeline addFirst(EventExecutorGroup group, String name, ChannelHandler handler) {
        final AbstractChannelHandlerContext newCtx;
        synchronized (this) {
            checkMultiplicity(handler);
            name = filterName(name, handler);

            newCtx = newContext(group, name, handler);

            addFirst0(newCtx);

            // If the registered is false it means that the channel was not registered on an eventLoop yet.
            // In this case we add the context to the pipeline and add a task that will call
            // ChannelHandler.handlerAdded(...) once the channel is registered.
            if (!registered) {
                newCtx.setAddPending();
                callHandlerCallbackLater(newCtx, true);
                return this;
            }

            EventExecutor executor = newCtx.executor();
            if (!executor.inEventLoop()) {
                callHandlerAddedInEventLoop(newCtx, executor);
                return this;
            }
        }
        callHandlerAdded0(newCtx);
        return this;
    }

    private void addFirst0(AbstractChannelHandlerContext newCtx) {
        AbstractChannelHandlerContext nextCtx = head.next;
        newCtx.prev = head;
        newCtx.next = nextCtx;
        head.next = newCtx;
        nextCtx.prev = newCtx;
    }

    @Override
    public final ChannelPipeline addLast(String name, ChannelHandler handler) {
        return addLast(null, name, handler);
    }

    @Override
    public final ChannelPipeline addLast(EventExecutorGroup group, String name, ChannelHandler handler) {
        final AbstractChannelHandlerContext newCtx;
        synchronized (this) {
            // 检查Handler是否重复添加
            checkMultiplicity(handler);

            // 新建一个Context
            newCtx = newContext(group, filterName(name, handler), handler);

            // 实际的双向链表插入操作
            addLast0(newCtx);

            // If the registered is false it means that the channel was not registered on an eventLoop yet.
            // In this case we add the context to the pipeline and add a task that will call
            // ChannelHandler.handlerAdded(...) once the channel is registered.
            // 如果注册为false，则意味着channel尚未在eventLoop上注册。
            // 假若这样，我们将context添加到pipeline中，并添加一个任务，该任务将在注册通道后调用ChannelHandler.handlerAdded(…)。
            if (!registered) {
                // 此时Channel还没注册的EventLoop中，而Netty的原则是事件在同一个EventLoop执行，
                // 所以新增一个任务用于注册后添加
                newCtx.setAddPending();
                callHandlerCallbackLater(newCtx, true);
                return this;
            }

            EventExecutor executor = newCtx.executor();
            if (!executor.inEventLoop()) {
                // 当前线程不是EventLoop线程
                callHandlerAddedInEventLoop(newCtx, executor);
                return this;
            }
        }
        // 当前线程为EventLoop线程且已注册则直接触发HandlerAdd事件
        callHandlerAdded0(newCtx);
        return this;
    }

    /**
     * DefaultChannelPipeline的双向链表初始化持有头部和尾部节点，这两个节点对用户不可见，
     * 也就是说，用户addLast只是将节点插入尾部节点之前，addFirst将节点插入头部节点之后。
     */
    private void addLast0(AbstractChannelHandlerContext newCtx) {
        AbstractChannelHandlerContext prev = tail.prev;
        newCtx.prev = prev;
        newCtx.next = tail;
        prev.next = newCtx;
        tail.prev = newCtx;
    }

